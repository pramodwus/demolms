package com.qbis.model;

public class Minute {
	
	private Integer min;
	private Integer second;
	private Integer sessionView;
	public Integer getSessionView() {
		return sessionView;
	}
	public void setSessionView(Integer sessionView) {
		this.sessionView = sessionView;
	}
	public Integer getMin() {
		return min;
	}
	public void setMin(Integer min) {
		this.min = min;
	}
	public Integer getSecond() {
		return second;
	}
	public void setSecond(Integer second) {
		this.second = second;
	}
	

}
